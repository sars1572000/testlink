<p>列出各個構建的執行結果. 對於每一個構建, 有總測試用例數, 總通過數, 通過的比例, 總失敗數, 失敗的比例, 總受阻數, 受阻的比例, 未執行的總數, 未執行的比例. 如果一個測試用例在同一構建版本上執行了多次, 則最近一次的結果才計入統計結果.</p>

<h3>查詢統計</h3>
<p>該報表包括一個查詢輸入表單, 一個查詢結果表單. 查詢輸入表單有四個按鈕. 每個按鈕的缺省值設置為查詢可以包括的最大範圍. 用戶可以更改按鈕以縮小查詢範圍. 可以按執行人, 關鍵字, 子類, 構建等組合過濾.</p>

<ul>
<li><b>關鍵字</b>可以選擇 0->1 個關鍵字. 系統缺省的設置是不選. 如果該關鍵字不被選中, 則不管測試規範裡和關鍵字管理頁中有沒有分配該關鍵字,所有測試用例都被認為忽略了關鍵字的關聯. 一個關鍵字指派到一個測試用例後, 將傳播到該測試用例所屬於的所有的測試計劃, 以及該測試用例的所有版本. 如果你只關心包含特定的關鍵字的測試結果, 你需要修改控制按鈕的值. </li>
<li><b>所有者</b> 可以選擇 0->1 個所有者. 系統缺省的設置是不選. 如果不選. 則所有測試用例都將被選擇,不管測試任務分派給誰. 目前還沒有搜索'未指派'執行人的測試用例的功能. 所有權的問題是通過 '指派測試任務'頁來實現的, 而且是每個測試計劃都要單獨做的. 如果你關心工作是誰做的,你要修改這個按鈕的值.</li>
<li><b>頂級子集</b>可以選擇 0->n 級測試子集. 缺省狀態是所有子集.
    只有被選取的子集才出現在查詢結果中.如果你只關心某個子集,你可以修改這項控制.</li>
<li><b>構建</b> 可以選擇 1->n 個構建. 缺省狀態是選擇所有構建.統計報告只顯示你選取的構建中已執行過的測試結果. 例如, 如果你只想看到在過去的三個構建上做過多少次測試, 你可以修改這個按鈕. 關鍵字, 所有者, 頂級子集的這三項的選擇決定了計入統計數據中的測試用例數目. 例如, 如果你選擇了執行者= '張三', 關鍵字 = '優先 1', 以及所有的子集- 那麼只有分派給張三的優先級為 1 的測試用例被計算在內. 報表中看到的測試用例的總數目會隨著這三個過濾按鈕給出的條件的不同而不同. 構建過濾只對'通過', '失敗', '受阻', 或者'未執行' 的測試用例有作用. 參見上面關於最後測試結果的說明.</li>
</ul>
<p>點擊\"提交\"按鈕啟動查詢和輸出顯示的文件</p>

