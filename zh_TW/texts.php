<li>測試計劃的全部參數</li>
<li>顯示了一個測試套件裡所有執行的結果和按(總和/通過/失敗/受阻/未執行)的分類結果. 如果一個測試用例在多個版本上執行過多次, 各次執行的結果都會顯示出來. 然而在該測試套件的總結裡, 只有選定版本的測試結果才會被顯示出來.</li>
</ol>

