<h3>通用測試統計報告</h3>
<p>該頁只顯示當前測試計劃中的測試套件,所有者, 關鍵字的最新狀態.
'當前狀態' 是指最新構建測試版本的執行狀態.例如. 一個測試用例在多個構建版本上執行過. 這裡只顯示最新版本的結果.</p>

<p>'最終測試結果'是許多報告裡用的一個概念. 它是這樣定義的:</p>
<ul>
<li>構建版本加入到測試計劃裡的先後順序決定了哪個構建版本是最新的. 最新構建版本中的測試結果比舊版本的測試結果優先. 例如, 如果你在版本1里記錄了一個測試用例的測試結果是』失敗』. 你在版本2里記錄同一個測試用例的測試結果是'通過',則最終的測試結果是 '通過'.</li>
<li>如果同一個測試用例在同一個構建版本上執行了多次. 那麼最後一次執行的結果優先. 例如. 如果版本 3 發佈了. 你的團隊裡的 tester 1 在 2:00pm 報告結果為'通過',而 tester 2 在 3:00pm 報告結果為'失敗'- 則最終結果顯示為'失敗'.</li>
<li>在某個版本裡顯示為'未執行'的測試用例不會覆蓋上一次的測試結果. 例如, 如果你在版本 1 中測試結束後記錄為'通過', 在版本2里還沒有執行, 則顯示的最終結果是'通過'.
</li>
</ul>
<p>顯示的列表:</p>
<ul>
	<li><b>按頂級測試套件</b>
